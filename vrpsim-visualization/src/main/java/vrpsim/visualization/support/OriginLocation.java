package vrpsim.visualization.support;

import vrpsim.core.model.network.impl.Location;

public class OriginLocation {

	private final double x;
	private final double y;

	public OriginLocation(Location location) {
		this.x = location.getX();
		this.y = location.getY();
	}
	
	public OriginLocation(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

}
